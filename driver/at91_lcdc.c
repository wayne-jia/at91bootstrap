struct dma_desc {
	u32 addr;
	u32 ctrl;
	u32 next;
	u32 reserved;
};

struct lcdc_desc {
	u16 width;     /**< Display image width */
	u16 height;    /**< Display image height */
	u16 framerate; /**< Frame rate in Hz */

	u16 timing_vfp; /**< Vertical front porch in number of lines */
	u16 timing_vbp; /**< Vertical back porch in number of lines */
	u16 timing_vpw; /**< Vertical pulse width in number of lines */
	u16 timing_hfp; /**< Horizontal front porch in LCDDOTCLK cycles */
	u16 timing_hbp; /**< Horizontal back porch in LCDDOTCLK cycles */
	u16 timing_hpw; /**< Horizontal pulse width in LCDDOTCLK cycles */

	u16 ovr_mode;
	u16 ovr_width;
	u16 ovr_height;
	u16 ovr_sc_width;
	u16 ovr_sc_height;
	u16 ovr_xpos;
	u16 ovr_ypos;
	u32 ovr_xstride;
	u32 ovr_pstride;
	struct dma_desc *ovr_dma;
	u8 *ovr_buf;

	u32 clut_size;

	struct bmp_desc *bmp;
};

typedef enum {
	BI_RGB       = 0x0000,
	BI_RLE8      = 0x0001,
	BI_RLE4      = 0x0002,
	BI_BITFIELDS = 0x0003,
	BI_JPEG      = 0x0004,
	BI_PNG       = 0x0005,
	BI_CMYK      = 0x000B,
	BI_CMYKRLE8  = 0x000C,
	BI_CMYKRLE4  = 0x000D
} Compression;

struct bit_fields {
	u32 r_mask;
	u32 g_mask;
	u32 b_mask;
} __attribute__ ((packed, aligned(1)));

struct bmp_desc {
	u8  bf_type[2];
	u32 bf_size;
	u16 bf_reserved1;
	u16 bf_reserver2;
	u32 bf_offbits;

	u32 bi_size;
	u32 bi_width;
	u32 bi_height;
	u16 bi_planes;
	u16 bi_bitcount;
	u32 bi_compression;
	u32 bi_sizeimage;
	u32 bi_xpelspermeter;
	u32 bi_ypelspermeter;
	u32 bi_clrused;
	u32 bi_clrimportant;
	u32 clut[];
} __attribute__ ((packed, aligned(1)));

